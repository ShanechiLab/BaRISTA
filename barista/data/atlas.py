import enum

UNKNWON_STR = "UNKNOWN"


class EnumWithUnknown(enum.Enum):
    @classmethod
    def get_enum(cls, value):
        value = (value or UNKNWON_STR).upper()
        try:
            return cls[value]
        except KeyError as e:
            raise NotImplementedError(
                f"Unknown value '{value}' for enum {cls.__name__}"
            ) from e


